def check(a, b, start, end, ans):
    if start <= a <= end or start <= b <= end:
        return 'NO'
    elif start > a < end < b > start:
        return 'NO'
    else:
        return ans


def to_time(str1):
    return datetime.strptime(str1, '%H:%M:%S').time()


